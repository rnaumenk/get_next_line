int		ft_intlen(int nbr)
{
	int len;

	len = 1;
	if (nbr == -2147483648)
	{
		len++;
		nbr = nbr / 10;
	}
	if (nbr < 0)
	{
		nbr = nbr * -1;
		len++;
	}
	while (nbr > 9)
	{
		nbr = nbr / 10;
		len++;
	}
	return (len);
}
