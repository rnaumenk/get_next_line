int		ft_max(int *tab, unsigned int len)
{
	unsigned int	i;
	int				t;

	if (!len || !tab)
		return (0);
	i = 0;
	t = tab[0];
	while (i < len)
	{
		if (t < tab[i])
			t = tab[i];
		i++;
	}
	return (t);
}
